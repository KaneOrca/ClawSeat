def test_install_sh_multi_render_loads_via_runtime_loader(env_home, tmp_path):
    """install.sh --mode multi must produce a project.toml that the runtime
    profile loader accepts without KeyError. Audit fix 3: this test uses the
    real install.sh canonical entrypoint (not the internal install_multi.sh)."""
    project = "sts-dogfood"
