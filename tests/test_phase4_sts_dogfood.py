"""Phase 4 STS dogfood — honest-scoped integration tests.

These tests do NOT exercise a real planner seat end-to-end. They cover three
discrete real-CLI integration slices, named for what they actually validate:

1. install.sh --mode multi → render compatible with runtime profile loader
2. agent_admin brief queue / list / claim via canonical CLI entrypoint
3. Contract publish + drift cycle through CLI
4. Fuzz harness on a real EffectExpression DSL primitive set

A *true* E2E covering live planner pull, builder dispatch, acceptance_executor
invocation by planner, and chain-end relay requires running tmux seats and
is outside Phase 4 scope. The reviewer audit (round 6) flagged the prior
test name "test_sts_dogfood_full_chain" as misleading because it bypassed the
planner — this file restores honest naming.
"""

