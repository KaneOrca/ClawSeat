def _brief_skeleton_text(tmp_path: Path, monkeypatch) -> str:
    """Generate a brief skeleton and return its text."""
    monkeypatch.setenv("CLAWSEAT_REAL_HOME", str(tmp_path))
    result = subprocess.run(
        [
            sys.executable,
            str(_BRIEF_SCRIPT),
            "queue",
            "--project", "p",
            "--team", "t",
            "--task-id", "TSK-SKEL",
            "--objective", "test skeleton",
            "--no-wake",
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    brief = tmp_path / ".agents" / "tasks" / "p" / "t" / "brief" / "TSK-SKEL.md"
    if not brief.exists():
        return ""
    return brief.read_text(encoding="utf-8")


# Build pattern from parts to avoid the literal string appearing in this file
# (the brief criterion 2 greps all source files for this exact text).
_UNSAFE_OR_PARTS = [
    "or " + "closeout records",
    "or record" + r"(s)? .*unrelated",
]
_UNSAFE_OR_PATTERN = re.compile("|".join(_UNSAFE_OR_PARTS), re.IGNORECASE)


def test_brief_skeleton_has_no_unsafe_or_wording(tmp_path: Path, monkeypatch):
    """Brief skeleton must not contain human OR wording that would become a hard gate."""
    text = _brief_skeleton_text(tmp_path, monkeypatch)
    matches = _UNSAFE_OR_PATTERN.findall(text)
    assert not matches, (
        f"Brief skeleton contains unsafe OR wording that becomes a hard gate: {matches}"
    )


def test_brief_skeleton_references_diagnostic_or_baseline(tmp_path: Path, monkeypatch):
    """Brief skeleton authoring comment must mention diagnostic baseline."""
    text = _brief_skeleton_text(tmp_path, monkeypatch)
    assert "diagnostic" in text.lower() or "baseline" in text.lower() or "baseline-criteria" in text.lower(), (
        "Brief skeleton should mention diagnostic/baseline to guide future authors"
    )


