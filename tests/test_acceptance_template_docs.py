"""CF044: Template and docs discoverability tests for diagnostic baseline semantics.

Verifies that:
- brief.schema.json accepts diagnostic:true in mechanical criterion objects.
- Brief skeleton generated by agent_admin_brief does not contain unsafe
  human-language OR qualifiers that become unenforced hard gates.
- The acceptance-criteria guide exists and documents key baseline concepts.
- The --baseline-criteria CLI arg is discoverable via acceptance run --help.
"""
from __future__ import annotations

import json
import re
import subprocess
import sys
from pathlib import Path

import pytest

REPO_ROOT = Path(__file__).resolve().parents[1]
_SCHEMA_PATH = REPO_ROOT / "core" / "schemas" / "brief.schema.json"
_GUIDE_PATH = REPO_ROOT / "docs" / "guides" / "acceptance-criteria.md"
_BRIEF_SCRIPT = REPO_ROOT / "core" / "scripts" / "agent_admin_brief.py"
_AGENT_ADMIN = REPO_ROOT / "core" / "scripts" / "agent_admin.py"


# ---------------------------------------------------------------------------
# Schema: diagnostic field recognized
# ---------------------------------------------------------------------------

def test_schema_mechanical_item_accepts_diagnostic_field():
    """brief.schema.json must declare 'diagnostic' as a property of mechanical criterion objects."""
    schema = json.loads(_SCHEMA_PATH.read_text(encoding="utf-8"))
    mech_items = (
        schema["properties"]["acceptance_criteria"]["properties"]["mechanical"]["items"]
    )
    obj_schema = next(
        (s for s in mech_items["anyOf"] if s.get("type") == "object"),
        None,
    )
    assert obj_schema is not None, "mechanical items must include an object variant"
    assert "diagnostic" in obj_schema.get("properties", {}), (
        "brief.schema.json must declare 'diagnostic' property in mechanical criterion objects"
    )


def test_schema_diagnostic_field_is_boolean():
    schema = json.loads(_SCHEMA_PATH.read_text(encoding="utf-8"))
    mech_items = schema["properties"]["acceptance_criteria"]["properties"]["mechanical"]["items"]
    obj_schema = next(s for s in mech_items["anyOf"] if s.get("type") == "object")
    diag = obj_schema["properties"]["diagnostic"]
    assert diag.get("type") == "boolean", f"diagnostic must be boolean, got {diag}"


def test_schema_mechanical_description_warns_against_or_clauses():
    """The mechanical array description should warn that human OR clauses are not enforceable."""
    schema = json.loads(_SCHEMA_PATH.read_text(encoding="utf-8"))
    mech_desc = (
        schema["properties"]["acceptance_criteria"]["properties"]["mechanical"]
        .get("description", "")
    )
    assert "deterministic" in mech_desc.lower() or "machine" in mech_desc.lower(), (
        "mechanical description should mention deterministic/machine-deterministic requirement"
    )


