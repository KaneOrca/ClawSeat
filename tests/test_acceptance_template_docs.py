# ---------------------------------------------------------------------------
# Codebase sweep: no unsafe OR wording in templates/docs/scripts
# ---------------------------------------------------------------------------

_SEARCH_DIRS = ["core", "docs", "scripts", "tests"]


def test_no_unsafe_or_wording_in_clawseat_source():
    """None of the ClawSeat source directories may contain unsafe human OR qualifier wording.

    Regression: catches future drift where the problematic natural-language qualifier
    pattern reappears in templates, docs, or generated scripts.
    """
    found = []
    for dir_name in _SEARCH_DIRS:
        directory = REPO_ROOT / dir_name
        if not directory.is_dir():
            continue
        for path in directory.rglob("*.py"):
            if "__pycache__" in path.parts:
                continue
            try:
                text = path.read_text(encoding="utf-8", errors="replace")
            except OSError:
                continue
            if _UNSAFE_OR_PATTERN.search(text):
                found.append(str(path))
        for path in directory.rglob("*.md"):
            if "__pycache__" in path.parts:
                continue
            try:
                text = path.read_text(encoding="utf-8", errors="replace")
            except OSError:
                continue
            if _UNSAFE_OR_PATTERN.search(text):
                found.append(str(path))
    assert not found, f"Unsafe OR wording found in: {found}"
